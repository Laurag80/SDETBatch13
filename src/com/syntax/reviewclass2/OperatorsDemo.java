package com.syntax.reviewclass2;

public class OperatorsDemo {

	public static void main(String[] args) {
		
		// Operators in Java 
		//Arithmatics Operators+ - / * % - answer will be numbers
		//Logical Operators == True or false == != >= <= > <
		
		int num=10;
		int num2=10;
		System.out.println(num+num2);
		System.out.println(num>=num2);//num>num2=>f or num=num2=>t
		//Whole 0-infinity Negative infinity to Positive infinity
		System.out.println(Long.MAX_VALUE);//Give us the maximum range
		System.out.println(Long.MIN_VALUE);//Give us the minimum range
		System.out.println(Byte.MAX_VALUE);
		System.out.println(Integer.MAX_VALUE);
		System.out.println(num/num2);
		System.out.println(num%num2);
		
		System.out.println(10/3);
		System.out.println(10%3);
		
		System.out.println(10.0/3);
		System.out.println((int)10.0/3);
		System.out.println(10.0*3);
		
		
		
		
	}

}
